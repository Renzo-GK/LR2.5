#Если в кортеже есть хотя бы одна пара одинаковых соседних элементов, то напечатать все
#элементы, следующие за элементами первой из таких пар.

# Ввод кортежа
elements = input("Введите элементы кортежа, разделенные запятыми: ")
my_tuple = tuple(map(str.strip, elements.split(',')))

# Находим первую пару одинаковых соседних элементов
found = False
for i in range(len(my_tuple) - 1):
    if my_tuple[i] == my_tuple[i + 1]:
        found = True
        # Печатаем все элементы после найденной пары
        print("Элементы после первой пары одинаковых соседей:", my_tuple[i + 2:])
        break

if not found:
    print("В кортеже нет одинаковых соседних элементов.")
